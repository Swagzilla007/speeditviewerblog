export function getStatusColor(status: string): string {
  switch (status) {
    case 'published':
    case 'approved':
      return 'success';
    case 'draft':
    case 'pending':
      return 'warning';
    case 'archived':
    case 'rejected':
      return 'error';
    default:
      return 'secondary';
  }
}

